        <h2>Accès</h2>
        <p>
          Le point de départ de cette randonnée est la Richardière, un tout
          petit hameau situé au pied du Mont Aiguille. Le plus simple pour s'y
          rendre est de prendre le TER qui fait la liaison entre Grenoble et Gap
