        </p>
        <h2>De la Richardière au Pas de la Selle</h2>
        <p>
          Depuis la Richardière, prendre tout droit (plein ouest, vers les
          montagnes) sur la piste jusqu'à rejoindre un chalet moderne, le Foyer
